# can you change the data type of a variable after its been assigned? 
# explain with code example where a variable data type is changed

'''ans: yes you can change the data type of a variable after its been assigned .

for eg: '''

x = 10
print(type(x))  # Output: <class 'int'>

x = 10.5
print(type(x))  # Output: <class 'float'>

x = "ishan"
print(type(x))  # Output: <class 'str'>
